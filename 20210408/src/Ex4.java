
public class Ex4 {

	public static void main(String[] args) {
		int i = 0;
		++i; // 선행연산 : i = i + 1
		System.out.println(i);
		++i;
		System.out.println(i);
		++i;
		System.out.println(i);
		
		int j = 1;
		System.out.println(++j);
		int k = 1;
		System.out.println(k++);
		System.out.println(k);
		

	}

}
