
public class Ex6 {
	public static void main (String [] args) {
		//논리형 리터럴 true, false
		boolean b = true;
		System.out.println(b); //true
		System.out.println(!b); //false
		System.out.println(!!b); //true
		
		boolean b1 = false;
		System.out.println(b1); //false
		System.out.println(!b1); //true
		
		System.out.println(!true);//false
		System.out.println(!false);//true
	}

}
