
public class Ex3 {

	public static void main(String[] args) {
		int i = 0;
		i = i + 1; // i = 0 + 1
		System.out.println(i);
		i = i + 1; // i = 1 + 1
		System.out.println(i);
		i = i + 1; // i = 2 + 1
		System.out.println(i);
		
		int j = 0;
		j++; // 후행 연산 / j = j + 1 
		System.out.println(j);
		j++;
		System.out.println(j);
		j++;
		System.out.println(j);
		
	}

}
